public class Convertidor {
    public double MillasAKilometro(double millas) {
        return millas * 1.60934;
    }
    public double KilometroAMillas(double kms) {
        return kms / 1.60934;
    }
    public double KilogramosALibras(double kilos) {
        return kilos * 2.20462;
    }
    public double LibrasAKilogramos(double libras) {
        return libras / 2.20462;
    }
}
